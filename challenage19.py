# Simple Python type casting program

# Casting a string to an integer
str_num = "24"
int_num = int(str_num)
print(f"String '{str_num}' cast to Integer: {int_num}")

# Casting a string to a float
str_num = "24.5"
float_num = float(str_num)
print(f"String '{str_num}' cast to Float: {float_num}")

# Casting an integer to a float
int_num = 17
float_num = float(int_num)
print(f"Integer {int_num} cast to Float: {float_num}")

# Casting a float to an integer
float_num = 22.01
int_num = int(float_num)
print(f"Float {float_num} cast to Integer: {int_num}")

# Casting an integer to a string
int_num = 22
str_num = str(int_num)
print(f"Integer {int_num} cast to String: '{str_num}'")
